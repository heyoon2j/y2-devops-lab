#!/bin/bash

set -e

#######################################################
#####                Local Variable               #####
#######################################################
OS_ID=$1

GITHUB_URL=""
MY_PATH="/opt/"

#######################################################
#####               Function - Main               #####
#######################################################
main() {
  echo "========== Monitoring Setting Start =========="

  cd "$MY_PATH"
  git clone "$GITHUB_URL"
}


#######################################################
#####                                             #####
#######################################################
if [ -z "$OS_ID" ]; then
  echo "❗ 사용법: $0 <os: ubuntu|rocky8|rocky9>"
  exit 1
fi

main